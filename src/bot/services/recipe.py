"""Сервис для генерации рецептов из списка продуктов с использованием Polza.ai."""

import logging
from typing import List, Dict

from .polza_chat import ask_polza

logger = logging.getLogger(__name__)


async def generate_recipe(ingredients: str) -> str:
    """Генерирует рецепт на основе списка продуктов.

    Args:
        ingredients: Строка с перечислением продуктов (например, "картофель, лук, яйца").

    Returns:
        Текст рецепта.
    """
    if not ingredients.strip():
        return "Вы не указали продукты. Пожалуйста, перечислите продукты, которые у вас есть."

    # Формируем промпт для генерации рецепта
    messages: List[Dict[str, str]] = [
        {
            "role": "system",
            "content": (
                "Ты — профессиональный шеф-повар. "
                "Пользователь предоставляет список продуктов, которые есть у него в холодильнике. "
                "Твоя задача — предложить подробный рецепт блюда, которое можно приготовить из этих продуктов. "
                "Учти, что у пользователя могут быть базовые специи (соль, перец, растительное масло). "
                "Рецепт должен включать:\n"
                "1. Название блюда.\n"
                "2. Ингредиенты с приблизительными количествами.\n"
                "3. Пошаговую инструкцию приготовления.\n"
                "4. Советы по подаче (по желанию).\n"
                "Будь креативным, но реалистичным. Если продуктов недостаточно для полноценного блюда, предложи вариант с минимальными дополнениями. "
                "Отвечай только рецептом, без лишних комментариев."
            )
        },
        {
            "role": "user",
            "content": f"Вот продукты, которые у меня есть: {ingredients}"
        }
    ]

    try:
        recipe = await ask_polza(messages)
        # Убираем возможные лишние пробелы
        return recipe.strip()
    except Exception as e:
        logger.error("Ошибка при генерации рецепта: %s", e)
        return f"Ошибка генерации рецепта: {e}"