"""Функции для подготовки текстов ответов бота."""


def build_welcome_text(user_first_name: str) -> str:
    """Формирует приветственное сообщение.

    Никаких Telegram-зависимостей, только работа со строками.
    """
    name = user_first_name.strip() or "друг"
    return (
        f"Привет, {name}! 👋\n"
        "Я многофункциональный бот с различными командами.\n\n"
        "📋 **Основные команды:**\n"
        "- /start — приветствие и описание возможностей\n"
        "- /help — подробная справка по всем командам\n"
        "- /echo [текст] — повторю указанный текст\n"
        "- /echo_on — включить эхо обычных сообщений\n"
        "- /echo_off — выключить эхо обычных сообщений\n"
        "- /plus1 [число] — увеличу число на 1\n"
        "- /plus3_input — ввести число и прибавить 3\n"
        "- /cancel — отмена любого режима ввода\n\n"
        "🌐 **Дополнительные функции:**\n"
        "- /translate [текст] — перевести текст на английский\n"
        "- /recipe [продукты] — предложить рецепт из указанных продуктов\n\n"
        "🤖 **Режим общения с ChatGPT (Polza.ai):**\n"
        "- /chatgpt — войти в режим диалога с ИИ\n"
        "- /chatgpt_off — выйти из режима диалога\n"
        "В этом режиме вы можете вести беседу с искусственным интеллектом, задавать вопросы и получать развернутые ответы.\n\n"
        "Просто отправьте мне любое текстовое сообщение, и я его повторю (если эхо включено)."
    )


def build_help_text() -> str:
    """Формирует текст подсказки по использованию бота."""
    return (
        "Вот что я умею:\n"
        "- /start — приветствие и краткое описание;\n"
        "- /help — эта подсказка;\n"
        "- /echo текст — повторю указанный текст;\n"
        "- /echo_off — выключу эхо обычных сообщений;\n"
        "- /echo_on — включу эхо обычных сообщений;\n"
        "- /plus1 число — увеличу число на 1;\n"
        "- /plus3_input — попрошу ввести число и прибавлю 3;\n"
        "- /cancel — отмена режима ввода числа;\n"
        "- /chatgpt — войти в режим диалога с ИИ (Polza.ai);\n"
        "- /chatgpt_off — выйти из режима диалога;\n"
        "- /translate [текст] — перевести текст на английский язык;\n"
        "- /recipe [продукты] — предложить рецепт из указанных продуктов;\n"
        "- любое текстовое сообщение — просто повторю его."
    )


def build_echo_text(user_text: str) -> str:
    """Формирует ответ для эхо-сообщения.

    Если текст пустой, возвращает понятное сообщение.
    """
    cleaned = user_text.strip()
    if not cleaned:
        return "Ты ничего не написал, поэтому мне нечего повторять 🙂"
    return cleaned


def build_plain_text_reply(user_text: str) -> str:
    """Формирует ответ на обычное текстовое сообщение.

    Если пользователь прислал целое число, бот отвечает числом + 1.
    Во всех остальных случаях — обычное эхо через `build_echo_text`.
    """
    cleaned = user_text.strip()
    if not cleaned:
        # Пустой ввод обрабатываем так же, как и раньше в эхо.
        return build_echo_text(cleaned)

    try:
        value = int(cleaned)
    except ValueError:
        # Не число — обычное эхо.
        return build_echo_text(cleaned)

    # Если это целое число — возвращаем число + 1.
    return str(value + 1)
