"""Математические функции для бота (бизнес-логика).

Здесь нет зависимостей от Telegram/aiogram — только чистые функции.
"""


def add_three(value: int) -> int:
    """Увеличивает число на 3.

    Args:
        value: Целое число, которое нужно увеличить.

    Returns:
        Число `value + 3`.
    """

    return value + 3

